DROP MASKING POLICY IF EXISTS SNOWFLAKE_EXAMPLE.DATA_QUALITY.CONFIDENTIAL_STRING_MASK;

DROP VIEW IF EXISTS SNOWFLAKE_EXAMPLE.DATA_QUALITY.V_TAG_GOVERNANCE_SUMMARY;
DROP TAG IF EXISTS SNOWFLAKE_EXAMPLE.DATA_QUALITY.DATA_DOMAIN;
DROP TAG IF EXISTS SNOWFLAKE_EXAMPLE.DATA_QUALITY.DATA_SENSITIVITY;

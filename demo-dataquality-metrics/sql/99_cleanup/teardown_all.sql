DROP STREAM IF EXISTS SNOWFLAKE_EXAMPLE.DATA_QUALITY.RAW_ATHLETE_PERFORMANCE_STREAM;
DROP STREAM IF EXISTS SNOWFLAKE_EXAMPLE.DATA_QUALITY.RAW_FAN_ENGAGEMENT_STREAM;

DROP VIEW IF EXISTS SNOWFLAKE_EXAMPLE.DATA_QUALITY.V_QUALITY_SCORE_TREND;
DROP VIEW IF EXISTS SNOWFLAKE_EXAMPLE.DATA_QUALITY.V_DATA_QUALITY_METRICS;

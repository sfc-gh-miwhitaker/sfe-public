DROP TABLE IF EXISTS SNOWFLAKE_EXAMPLE.DATA_QUALITY.RAW_FAN_ENGAGEMENT;
DROP TABLE IF EXISTS SNOWFLAKE_EXAMPLE.DATA_QUALITY.RAW_ATHLETE_PERFORMANCE;

DROP SCHEMA IF EXISTS SNOWFLAKE_EXAMPLE.DATA_QUALITY CASCADE;
DROP WAREHOUSE IF EXISTS SFE_DATA_QUALITY_WH;
DROP GIT REPOSITORY IF EXISTS SNOWFLAKE_EXAMPLE.GIT_REPOS.SFE_DATA_QUALITY_REPO;
